let a = 9;
let b = 22;

/* AND operator */
// angka 15 & 10 merupakan angka bebas, silahkan diisi sesuai kreasi kalian
console.log(a < 30 && b > 20); // (true && true) -> true
console.log(a > 30 && b > 20); // (false && true) -> false

/* OR operator */
console.log(a < 30 || b > 20); // (true || true) -> true
console.log(a > 30 || b > 20); // (false || true) -> true

/* NOT operator */
console.log(!(a < 30)); // !(true) -> false
console.log(!(a < 30 && b > 20)); // !(true && true) -> !(true) -> false

